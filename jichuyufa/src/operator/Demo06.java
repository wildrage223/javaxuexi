package operator;

public class Demo06 {
    public static void main(String[] args) {
        /*
        A= 0011 1100
        B= 0000 1101
        -------------------------------------------
        A&B A与B，上下两个比较都为1才为1    =0000 1100
        A|B A或B，对应位都为0则为0，有一个为1则为1   =0011 1101
        A^B AB取反，相同为0，异同为1  =0011 0001
        ~B 非B =1111 0010

        2*8 = 16  2*2*2*2 怎么最快  2<<3 2左移三位
        <<左移  >>右移   效率极高
         */

        System.out.println(2<<3);//16
    }
}
